package ru.demo.wms.service;

import java.util.List;
import ru.demo.wms.model.Document;

/**
 * Интерфейс IDocumentService определяет контракт для сервисного слоя,
 * отвечающего за управление объектами Document в приложении WMS.
 * <p>
 * Основные функции включают сохранение, получение, удаление документов
 * и отображение их идентификаторов и названий.
 */
public interface IDocumentService {

	/**
	 * Сохраняет документ в базу данных.
	 *
	 * @param doc объект Document, содержащий данные для сохранения
	 */
	void saveDocument(Document doc);

	/**
	 * Возвращает список пар [ID, Название документа].
	 * Используется, например, для отображения в выпадающих списках или таблицах.
	 *
	 * @return список массивов, где каждый массив содержит:
	 *         [0] - Long ID документа,
	 *         [1] - String имя/название документа
	 */
	List<Object[]> getDocumentIdAndName();

	/**
	 * Удаляет документ по его ID.
	 * При удалении следует убедиться в наличии документа.
	 *
	 * @param id уникальный идентификатор документа
	 */
	void deleteDocumentById(Long id);

	/**
	 * Получает документ по его идентификатору.
	 * Используется для отображения деталей документа или его загрузки.
	 *
	 * @param id уникальный идентификатор документа
	 * @return объект Document, если найден
	 */
	Document getDocumentById(Long id);
}
