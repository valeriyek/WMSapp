package ru.demo.wms.service;

import java.util.Map;

/**
 * Сервисный интерфейс для работы с ролями пользователей.
 *
 * Предоставляет метод для получения всех доступных ролей в виде отображения (Map),
 * где ключ — ID роли, а значение — её название.
 *
 * Используется в системах управления пользователями, доступа и интерфейсах администрирования.
 */
public interface IRoleService {

	/**
	 * Возвращает карту всех ролей в системе.
	 *
	 * @return отображение, где:
	 *         ключ — ID роли (Integer),
	 *         значение — название роли (String).
	 */
	Map<Integer, String> getRolesMap();
}
